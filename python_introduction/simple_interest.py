# Define the variables
principal = 1000  # P: principal amount ($1000)
rate = 0.05       # R: annual interest rate (5% or 0.05)
time = 3          # T: time period in years (3 years)

# Formula for Simple Interest (I = P * R * T)
interest = principal * rate * time

# Print the calculated simple interest in the required format
print(f"The simple interest is: {interest}")
